package operator;

public class UnaryOper {
    public static void main(String[] args) {
        double number = 5;
        double number2 = 10;

        System.out.println("number ->"+number);
        System.out.println("number++ ->"+ ++number);
        System.out.println("number ->"+ number);

        System.out.println("number2 ->"+ number2);
        System.out.println("number2-- ->" + --number2);
    }
}
